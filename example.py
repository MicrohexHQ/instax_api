import instax

print("Instax SP-2 Example App")
myInstax = instax.SP2()
#myInstax.connect(ip='localhost')
myInstax.connect()
print('--------------------------------------------------')
version = myInstax.getPrinterVersion()
version.dump()
print('--------------------------------------------------')
model = myInstax.getPrinterModel()
model.dump()
print('--------------------------------------------------')
printCount = myInstax.getPrintCount()
printCount.dump()
print('--------------------------------------------------')
printerSpecifications = myInstax.getPrinterSpecifications()
printerSpecifications.dump()
print('--------------------------------------------------')
